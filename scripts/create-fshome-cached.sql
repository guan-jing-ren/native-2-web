R"(
CREATE VIEW IF NOT EXISTS FSHOME_CACHED (
  PARENT,
  STEM,
  EXTENSION,
  FILESIZE,
  MODDATE,
  FILETYPE,
  MIMETYPE,
  PERMISSIONS
)
AS SELECT
  PARENT,
  STEM,
  EXTENSION,
  FILESIZE,
  MODDATE,
  FILETYPE,
  MIMETYPE,
  PERMISSIONS
FROM
  FSFILES_CACHED INNER JOIN
  FSSTATS_CACHED USING (FILEID) INNER JOIN
  FSPARENTS_CACHED USING (PARENTID)
)"
